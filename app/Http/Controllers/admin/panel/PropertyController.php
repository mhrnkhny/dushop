    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $properties  = propery::orderBy('id');
        if (\request()->filled('search')) {
            $properties = $properties->where('property_text', 'like', '%' . \request('search') . '%');
        }
        $properties = $properties->paginate(5);
        return view('panel.Products.property.index',compact('properties'));
    }

